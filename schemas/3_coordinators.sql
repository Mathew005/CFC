    Phone2 VARCHAR(50),
    Faculty2 BOOLEAN,
    Name3 VARCHAR(255),
    Email3 VARCHAR(255),
    Phone3 VARCHAR(50),
    Faculty3 BOOLEAN,
    Name4 VARCHAR(255),
    Email4 VARCHAR(255),
    Phone4 VARCHAR(50),
    Faculty4 BOOLEAN
